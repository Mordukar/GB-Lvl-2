<template>
  <div>
    <input type="text" placeholder="Имя" v-model="name"><br>
    <input type="text" placeholder="Цена" v-model="price">
    <Button @clicked="addGood">Добавить товар</Button>
  </div>
</template>

<script>
import {mapActions} from 'vuex';
import Button from './Button.vue';
export default {
  components: {
    Button,
  },
  data(){
    return {
      name: '',
      price: ''
    }
  },
  methods: {
    ...mapActions([
      'addItem'
    ]),
    addGood () {
      const goodToAdd = { name: this.name, price: this.price, img: '' }
      console.log(goodToAdd);
      this.addItem(goodToAdd)
    }
  },
}
</script>

